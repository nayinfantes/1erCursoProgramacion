/*Escribe un programa que calcule la circunferencia de un círculo. Declara la
constante Pi y una variable que almacene el radio.*/
public class CircunferenciaCirculo {
    // Las constantes se escriben en mayúsculas y
    // siempre son static final
    public static final double PI=3.14159;
        public static void main(String[] args)
        {
            int radio=2;
            // Utilizamos la fórmula (2 x PI x radio) para
            // calcular la circunferencia de un círculo
            System.out.println (2 * PI * radio);
        }
}
