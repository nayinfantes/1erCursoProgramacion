/*Haz un programa que escriba en pantalla los números del 1 al 10 utilizando
un bucle do while.*/
public class DoWhileLoop1 {
   public static void main(String[] args)
    {
        int i=1;
        // System.out.println (" Entro al do-while con i= " + i);
        do{
            System.out.println (i);
            i++;
            }
        while (i<=10);
        // System.out.println (" Después del do-while i= " + i);
    } 
}
